# 2. Функції обчислення та збереження
def calculate_score(answers):
    scores = {}
    total_score = 0
    
    for subscale, items in SUBSCALES.items():
        subscale_sum = 0
        for item in items:
            raw_score = answers[item]
            final_score = (6 - raw_score) if item in INVERTED_ITEMS else raw_score
            subscale_sum += final_score
            
        scores[subscale] = subscale_sum
        total_score += subscale_sum
        
    scores["ЗІМ"] = total_score
    return scores

def get_level(score, is_total=False):
    if is_total:
        if score >= 248: return "Високий"
        elif score >= 200: return "Вище середнього"
        elif score >= 152: return "Середній"
        elif score >= 104: return "Нижче середнього"
        else: return "Низький"
    else:
        if score >= 62: return "Високий"
        elif score >= 50: return "Вище середнього"
        elif score >= 38: return "Середній"
        elif score >= 26: return "Нижче середнього"
        else: return "Низький"

